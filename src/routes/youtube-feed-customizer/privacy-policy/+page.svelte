<h1 id="privacy-policy">Privacy Policy</h1>
<p
    >YouTube Feed Customizer is committed to protecting your privacy. This
    Privacy Policy explains how we collect, use, and safeguard your information
    when you use our Chrome extension.</p
>
<h3 id="information-collection-and-use">Information Collection and Use</h3>
<ol>
    <li
        ><p
            ><strong>Channel Preferences</strong>: We collect and store the
            names of YouTube channels you choose to include in your customized
            feed. This information is stored locally on your device and, if
            you&#39;re signed into Chrome, synced across your devices using
            Chrome&#39;s storage API.</p
        >
    </li>
    <li
        ><p
            ><strong>Usage Data</strong>: We do not collect any personal
            information, browsing history, or usage data beyond the channel
            names you explicitly provide.</p
        >
    </li>
</ol>
<h3 id="data-storage-and-security">Data Storage and Security</h3>
<ol>
    <li
        >All data is stored locally on your device using Chrome&#39;s secure
        storage API.</li
    >
    <li
        >If you&#39;re signed into Chrome, your channel list is synced across
        your devices using Chrome&#39;s secure sync functionality.</li
    >
    <li>We do not have access to this data, nor is it stored on our servers.</li
    >
</ol>
<h3 id="third-party-access">Third-Party Access</h3>
<ol>
    <li>We do not share, sell, or transfer your data to any third parties.</li>
    <li
        >Your data remains entirely under your control within the Chrome browser
        environment.</li
    >
</ol>
<h3 id="changes-to-youtube-com">Changes to YouTube.com</h3>
<ol>
    <li
        >Our extension modifies the appearance of YouTube.com on your device by
        hiding videos from channels not in your allowed list.</li
    >
    <li
        >These changes are made locally and do not affect YouTube&#39;s servers
        or other users&#39; experiences.</li
    >
</ol>
<h3 id="updates-and-changes">Updates and Changes</h3>
<p
    >We may update this Privacy Policy from time to time. We will notify you of
    any changes by posting the new Privacy Policy on the Chrome Web Store
    listing for YouTube Feed Customizer.</p
